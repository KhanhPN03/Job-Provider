package Model;

/**
 *
 * @author ADMIN
 */
public class Manager {

    private String m_id;
    private String a_id;
    private String m_name;

    public Manager() {
    }

    public Manager(String m_id, String a_id, String m_name) {
        this.m_id = m_id;
        this.a_id = a_id;
        this.m_name = m_name;
    }

    public String getM_id() {
        return m_id;
    }

    public void setM_id(String m_id) {
        this.m_id = m_id;
    }

    public String getA_id() {
        return a_id;
    }

    public void setA_id(String a_id) {
        this.a_id = a_id;
    }

    public String getM_name() {
        return m_name;
    }

    public void setM_name(String m_name) {
        this.m_name = m_name;
    }

}
