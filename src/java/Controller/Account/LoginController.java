    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        String username = request.getParameter("username");
        String password_raw = request.getParameter("password");

        hashByMD5 md5 = new hashByMD5();
        String password = md5.hash(password_raw);

        AccountDAO aDAO = new AccountDAO();
        Account a = aDAO.login(username, password);

        if (a != null) {
            if (a.getRole().equals("0")) {
                request.setAttribute("account", a);
                response.sendRedirect("PendingRequest");
            } else {
//                request.setAttribute("account", a);
                request.setAttribute("account", a);
                request.getRequestDispatcher("viewpostcontroller").forward(request, response);
            }
        } else {
            request.setAttribute("error", "Wrong username or password.");
            request.getRequestDispatcher("Login.jsp").forward(request, response);
        }
    }

